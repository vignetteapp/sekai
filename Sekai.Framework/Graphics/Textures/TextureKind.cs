namespace Sekai.Framework.Graphics.Textures;

/// <summary>
/// The kind of <see cref="Texture"/> that is being represented.
/// </summary>
public enum TextureKind
{
    /// <summary>
    /// A one-dimensional texture.
    /// </summary>
    Texture1D,

    /// <summary>
    /// A two-dimensional texture.
    /// </summary>
    Texture2D,

    /// <summary>
    /// A three-dimensional texture.
    /// </summary>
    Texture3D,
}
